let currentLang = 'ru';

window.setLanguage = function(lang) {
    if (!translations[lang]) {
        console.warn(`Language ${lang} not found, falling back to English`);
        lang = 'en';
    }
    currentLang = lang;
    document.documentElement.lang = lang;
    
    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            let text = translations[lang][key];
            
            // Handle variables if data-i18n-vars is present
            const varsAttr = el.getAttribute('data-i18n-vars');
            if (varsAttr) {
                try {
                    const vars = JSON.parse(varsAttr);
                    for (const [k, v] of Object.entries(vars)) {
                        text = text.replace(`{${k}}`, v);
                    }
                } catch (e) {
                    console.error('Error parsing i18n vars', e);
                }
            }
            
            el.textContent = text;
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
    
    // Dispatch event for other scripts that might need to know about language change
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

// Helper function for translating strings with variables
window.t = function(key, vars = {}) {
    if (!translations[currentLang] || !translations[currentLang][key]) {
        return key; // Fallback to key if not found
    }
    let text = translations[currentLang][key];
    for (const [k, v] of Object.entries(vars)) {
        text = text.replace(`{${k}}`, v);
    }
    return text;
};

// Initialize language on load if needed
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'ru';
    window.setLanguage(savedLang);
    
    const selector = document.getElementById('langSelector');
    if (selector) {
        selector.value = savedLang;
        selector.addEventListener('change', (e) => {
            const newLang = e.target.value;
            localStorage.setItem('preferredLanguage', newLang);
            window.setLanguage(newLang);
        });
    }
});
